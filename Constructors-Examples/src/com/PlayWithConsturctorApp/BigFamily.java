package com.PlayWithConsturctorApp;
// super(); --//--> 
//	This statement is used to call super class constructor inside a sub-class constructor.
// this(); ----//--->
// This statement  is used to call a constructor of same class inside another constructor.
// Note: There are only two ways to call constructor from super-class to sub-class and same class.

public class BigFamily // Super-Class
{
	BigFamily()
	{
		System.out.println("I'am Super-Class / BigFamily-Class Constructor...");
	}
}
